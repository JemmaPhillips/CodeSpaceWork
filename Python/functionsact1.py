def show_list_content(lst):
    print(f"The content of the list is: {lst}")

def find_max_value(lst):
    max_value = max(lst)
    print(f"The max value in the list: {max_value}")

#  List
list = [10, 2, 3, 4, 7]

# Call the functions with the sample list
show_list_content(list)
find_max_value(list)
